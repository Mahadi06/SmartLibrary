    private void addMemberBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMemberBtnActionPerformed
        // TODO add your handling code here:
        String mName = memberName.getText();
        String mPhone = memberPhone.getText();
        String mEmail = memberEmail.getText();
        String mAddress = memberAddress.getText();
       
        
        try {
            preStat = con.prepareStatement("insert into members (member_name,member_phone,member_email,member_address) VALUES (?, ?, ?, ?)");
            preStat.setString(1,mName);
            preStat.setString(2,mPhone);
            preStat.setString(3,mEmail);
            preStat.setString(4,mAddress);
            
            int n = preStat.executeUpdate();
            
            if(n == 1){
                JOptionPane.showMessageDialog(null,"New member Added");
                memberName.setText("");
                 memberPhone.setText("");
                memberEmail.setText("");
               memberAddress.setText("");
                loadMember();
            }else{
                JOptionPane.showMessageDialog(null,"Something is wrong");
                memberName.setText("");
                 memberPhone.setText("");
                memberEmail.setText("");
               memberAddress.setText("");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addMemberBtnActionPerformed

